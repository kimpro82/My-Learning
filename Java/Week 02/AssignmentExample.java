public class AssignmentExample {
	public static void main(String args[]) {
		int num = 17;
		System.out.println(num);
		num += 1;
		System.out.println(num);
		num -= 2;
		System.out.println(num);
		num *= 3;
		System.out.println(num);
		num /= 4;
		System.out.println(num);
		num %= 5;
		System.out.println(num);
		
		int num2 = 0;
		num2++;
		System.out.println(num2);
		++num2;
		System.out.println(num2);
		num2--;
		System.out.println(num2);
		--num2;
		System.out.println(num2);
	}
}
